package com.google.yash.Questions;

public class PINumberFormatting {
	
	public static void main(String[] args) {
		System.out.println(String.format("%.1f", Math.PI));
		System.out.println(String.format("%.2f", Math.PI));
		System.out.println(String.format("%.3f", Math.PI));
		System.out.println(String.format("%.4f", Math.PI));
		System.out.println(String.format("%.5f", Math.PI));
	}

}
